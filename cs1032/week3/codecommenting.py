option = input ("Please enter a value for choice: ") # gets a value from the user in string format, assigns it to variable option

if option == "a": # checks if the string option has the value a
    print("You choose the first option.") # prints string if above condition is satisfied

elif option == "b":# checks if the string option has the value b
    print("You choose the second option.") # prints string if above condition is satisfied

elif option == "c":# checks if the string option has the value c
    print("You choose the third option.") # prints string if above condition is satisfied

else: # if none of the above conditions are satisfied
    print("Invalid choice.") # prints string if above condition is satisfied

